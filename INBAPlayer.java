
public interface INBAPlayer {
	
	/**
	 * Returns the player's total rebounds
	 * @return total rebounds of player
	 */
	int getRebounds();
		
	/**
	 * Returns the player's total points
	 * @return total points of player
	 */
	int getPoints();
	
	/**
	 * Returns the player's total assists
	 * @return total assists of player
	 */
	int getAssists();
	
	/**
	 * Returns the player's total steals
	 * @return total steals of player
	 */
	int getSteals();
	
	/**
	 * Returns the player's total blocks
	 * @return total blocks of player
	 */
	int getBlocks();
	
	/**
	 * Returns the player's name
	 * @return name of player
	 */
	String getName();
	
	
}
